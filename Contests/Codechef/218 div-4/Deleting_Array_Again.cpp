#define nl "\n"

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n), c(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    for (int i = 0; i < n; i++)
        cin >> c[i];

    for (int i = 1; i < n; i++)
    {
        c[i] = min(c[i - 1], c[i]);
    }
    int ans = 0;
    for (int i = 0; i < n; i++)
    {
        ans += (a[i] * c[i]);
    }
    cout << ans << nl;
}

int main()
{
    fast;
    int t = 1;
    cin >> t;
    while (t--)
        solve();
    return 0;
}