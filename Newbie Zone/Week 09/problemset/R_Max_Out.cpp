void solve()
{
    int n;
    cin >> n;
    set<int> s;
    for (int i = 0; i < n; i++)
    {
        int x;
        cin >> x;
        s.insert(x);
    }
    if (s.size() < 2)
    {
        cout << 0 << nl;
    }
    else
    {
        auto it = s.rbegin();
        int a = *it;
        it++;
        int b = *it;
        cout << b % a << nl;
    }
}

